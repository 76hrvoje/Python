mood = input("Please enter your mood: ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Please mate, do not be sad")
elif mood == "excited":
    print ("Nice but be careful not to get over excited")
elif mood == "relaxed":
    print ("Grab Pina Colada and enjoy")
else:
    print("I do not recognize this mood!")